package com.google.android.apps.paco;

public class Pojo {

  
  private int x;
  private String y;
  public Pojo(int x, String y) {
    super();
    this.x = x;
    this.y = y;
  }
  public int getX() {
    return x;
  }
  public void setX(int x) {
    this.x = x;
  }
  public String getY() {
    return y;
  }
  public void setY(String y) {
    this.y = y;
  }
  
  
}
